// Оператор ||
// Выводит сразу 1е найденное им истинное значение(true value)

let a = 10 || 0;

console.log(a) // если выражение 1 истинна, то на выражение два мы не смотрим и выводим  1е выражение.



let b = 0 || 10;

console.log(b);   //В случае если 1 ложь, переходим ко второму если оно истинно выводим его